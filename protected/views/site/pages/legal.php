                    </li>
                    <li>
                        Member Patients must be at least 18 years old and present a current verifiable recommendation as well as a valid California Driver's License or California State issued ID card upon each visit to the collective.
                    </li>
                    <li>
                        Medical cannabis dispensaries may not dispense more than one ounce of dried cannabis per qualified patient or primary caregiver per visit to the medical cannabis dispensary. ( CAL HEATH &amp; SAFETY CODE 3309)
                    </li>
                    <li>
                        Upon entrance into the facility please turn off cell phones, additionally no cameras or recording devices may be used in the building.
                    </li>
                    <li>
                        Treat everyone in the building and our neighbors with respect.
                    </li>
                    <li>
                        Absolutely no alcohol, hard drugs, or weapons of any kind are allowed on the premises.
                    </li>
